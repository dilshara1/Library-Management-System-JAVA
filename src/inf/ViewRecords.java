    //Load Data to table
    public void setIBDetailsToTable() {

        try {
            conn = DbConnect.getConnection();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("Select * from issueBooks");

            while (rs.next()) {
                String id = rs.getString("id");
                String bookName = rs.getString("BookName");
                String memName = rs.getString("MemberName");
                Date iDate = rs.getDate("IssueDate");
                Date rDate = rs.getDate("ReturnDate");
                String status = rs.getString("Status");

                Object[] obj = {id, bookName, memName, iDate, rDate, status};
                model = (DefaultTableModel) tbl_allDetails.getModel();
                model.addRow(obj);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Add Correct Details");
        }

    }

