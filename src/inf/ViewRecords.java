    public void search() {

        Date id1 = iDate1.getDatoFecha();
        Date rd1 = iDate2.getDatoFecha();

        Long l1 = id1.getTime();
        Long l2 = rd1.getTime();

        java.sql.Date id2 = new java.sql.Date(l1);
        java.sql.Date rd2 = new java.sql.Date(l2);

        try {
            conn = DbConnect.getConnection();
            String sql = "select * from issueBooks where IssueDate BETWEEN ? and ? ";
            pst = conn.prepareStatement(sql);
            pst.setDate(1, id2);
            pst.setDate(2, rd2);
            
            rs = pst.executeQuery();
            if (rs.next() == false) {
                JOptionPane.showMessageDialog(this, "No Record Found");
            } else {
                while (rs.next()) {
                    String id = rs.getString("id");
                    String bookName = rs.getString("BookName");
                    String memName = rs.getString("MemberName");
                    Date iDate = rs.getDate("IssueDate");
                    Date rDate = rs.getDate("ReturnDate");
                    String status = rs.getString("Status");

                    Object[] obj = {id, bookName, memName, iDate, rDate, status};
                    model = (DefaultTableModel) tbl_allDetails.getModel();
                    model.addRow(obj);
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

